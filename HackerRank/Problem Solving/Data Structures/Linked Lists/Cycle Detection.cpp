bool has_cycle(SinglyLinkedListNode* head) {
    if (head == NULL || head->next == NULL) return false;
    SinglyLinkedListNode* slow = head, *fast = head;
    while (fast != NULL && fast->next != NULL) {
        slow = slow->next;
        fast = fast->next->next;
        if (slow == fast) {
            return true;
        }   
    }
    return false;
}

